/**
COMPONENTE CONTROLADO: De esta manera, los componentes React que rendericen un formulario también 
controlan lo que pasa en ese formulario con las subsecuentes entradas del usuario. Un campo de 
un formulario cuyos valores son controlados por React de esta forma es denominado “componente 
controlado”
 */
//FOrm nombre basico 
class NameForm extends React.Component {
  constructor(props) {
    super(props);
    this.state = {value: ''};

    this.handleChange = this.handleChange.bind(this);
    this.handleSubmit = this.handleSubmit.bind(this);
  }

  handleChange(event) {
    this.setState({value: event.target.value});
  }

  handleSubmit(event) {
    alert('A name was submitted: ' + this.state.value);
    event.preventDefault();
  }

  render() {
    return (
      <form onSubmit={this.handleSubmit}>
        <label>
          Name:
          <input type="text" value={this.state.value} onChange={this.handleChange} />
        </label>
        <input type="submit" value="Submit" />
      </form>
    );
  }
	
}
